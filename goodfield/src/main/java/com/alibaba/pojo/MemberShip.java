package com.alibaba.pojo;

import java.io.Serializable;

import org.codehaus.jackson.map.annotate.JsonSerialize;

/**
 * 会员信息实体类
 * 
 * @author mayn
 *
 */
@JsonSerialize(include = JsonSerialize.Inclusion.NON_NULL)
public class MemberShip implements Serializable {
	private static final long serialVersionUID = 1L;
	private String id; // 主键
	private String stores_id; // 门店id
	private String user_id; // 会员id
	private String user_name; // 用户名
	private String user_cards; // 卡号
	private String user_pic; // 用户头像
	private String user_type;	// 用户类型0通店 1单店
	private String member_type;	// 1会员0非会员
	private String exclusive;	// 专属问候语
	private String stores_name;
	private String userToken;
	private String loginStatus;
	private String aaa;
	private String face;
	
	public String getFace() {
		return face;
	}

	public void setFace(String face) {
		this.face = face;
	}

	public String getAaa() {
		return aaa;
	}

	public void setAaa(String aaa) {
		this.aaa = aaa;
	}

	public String getLoginStatus() {
		return loginStatus;
	}

	public void setLoginStatus(String loginStatus) {
		this.loginStatus = loginStatus;
	}

	public String getUserToken() {
		return userToken;
	}

	public void setUserToken(String userToken) {
		this.userToken = userToken;
	}

	public String getStores_name() {
		return stores_name;
	}

	public void setStores_name(String stores_name) {
		this.stores_name = stores_name;
	}

	public String getExclusive() {
		return exclusive == null ? "" : exclusive;
	}

	public void setExclusive(String exclusive) {
		this.exclusive = exclusive;
	}

	public String getUser_type() {
		return user_type;
	}

	public void setUser_type(String user_type) {
		this.user_type = user_type;
	}

	public String getMember_type() {
		return member_type;
	}

	public void setMember_type(String member_type) {
		this.member_type = member_type;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getStores_id() {
		return stores_id;
	}

	public void setStores_id(String stores_id) {
		this.stores_id = stores_id;
	}

	public String getUser_id() {
		return user_id;
	}

	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}

	public String getUser_name() {
		return user_name;
	}

	public void setUser_name(String user_name) {
		this.user_name = user_name;
	}

	public String getUser_cards() {
		return user_cards;
	}

	public void setUser_cards(String user_cards) {
		this.user_cards = user_cards;
	}

	public String getUser_pic() {
		return user_pic;
	}

	public void setUser_pic(String user_pic) {
		this.user_pic = user_pic;
	}

}
