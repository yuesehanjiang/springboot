package com.alibaba.pojo;

import java.io.Serializable;

public class NomemberImageLibrary implements Serializable {
	private static final long serialVersionUID = 1L;
	private String id; // 主键
	private String image_name; // 图片名
	private String photo_time; // 创建时间
	private String pic_path; // 图像路径

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getImage_name() {
		return image_name;
	}

	public void setImage_name(String image_name) {
		this.image_name = image_name;
	}

	public String getPhoto_time() {
		return photo_time == null ? null : photo_time.replace(".0", "");
	}

	public void setPhoto_time(String photo_time) {
		this.photo_time = photo_time;
	}

	public String getPic_path() {
		return pic_path;
	}

	public void setPic_path(String pic_path) {
		this.pic_path = pic_path;
	}

}
